#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

int	main(int count, char **args)
{
	int	i;

	i = 1;
	if (count > 1)
	{
		while (i < count)
		{
			ft_putstr(args[i++]);
			write(1, "\n", 1);
		}
	}
}
